void StratTraffic(std::ostream& oss , int duration, int burstSize, int slpTime)
{
    time_t    tx;
    struct tm tbuf, *temp;
    char buff[80];

    EapStats::GetEapStats()->ResetAll();

    /*
     * Set the duration, burst size and the sleep time
     */
    EapStats::duration = duration;
    EapStats::burstSize = burstSize;
    EapStats::slpTime = slpTime;

    /*
     * Store the start time
     */
    ftime(&EapStats::starter);
    time(&EapStats::startTime);

    EapStats::sendTraffic = ITS_TRUE;
    EapStats::firstTime = ITS_TRUE;
    time(&tx);
    temp =  localtime_r(&tx, &tbuf);
    strftime(buff, 80, "[ %b%d %Y %H:%M:%S  ]" , &tbuf);
    oss <<"Test Started at ----->" << buff << endl;

    /*
     * Set the traffic generator function
     */
    TrafficGeneratorFunc = SendEapDiameterEAPRequest;

}

void StopTraffic()
{
    EapStats::sendTraffic = ITS_FALSE;
    ftime(&EapStats::sendComplete);
}

